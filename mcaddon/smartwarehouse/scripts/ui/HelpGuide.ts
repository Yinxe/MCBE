/**
 * 向玩家发送帮助手册。
 * 通过聊天栏分页显示所有帮助内容。
 */
export function showHelpGuide(player: Player): void {
  for (const section of HELP_SECTIONS) {
    sendLines(player, section.content);
  }
}

/**
 * 根据分类索引发送特定章节的帮助内容。
 * @param player  目标玩家
 * @param sectionIndex 章节索引（0-3）
 */
export function showHelpSection(player: Player, sectionIndex: number): void {
  const section = HELP_SECTIONS[sectionIndex];
  if (!section) {
    player.sendMessage("§c帮助分类不存在");
    return;
  }
  sendLines(player, section.content);
}
